/*

1. What is OOP?
   - Object-Oriented Programming (OOP) is a programming paradigm that organizes code around objects, which are instances of classes. It promotes modular, reusable, and maintainable code by emphasizing concepts like encapsulation, inheritance, polymorphism,  and Abstraction.

2. Key Concepts of OOP:
   - Classes: A class is a blueprint or template that defines the properties and behavior of an object.

   - Objects: An Object is an instances of a class, created using the class definition.

   - Constructors: A constructor is a special method that is called when an object is created. It initializes the object's properties.

   - Inheritance: Inheritance is a mechanism that allows a class to inherit properties and methods from another class, called the superclass or parent class.

   - Polymorphism: Polymorphism is the ability of an object to take on multiple forms. It enables you to write generic code that can work with objects of multiple types, as long as they share a common interface.
      - Method Overriding (Runtime): Method overriding allows a subclass to provide a specific implementation of a method that is already defined in its superclass.
      - Method Overloading (Compile time): Method overloading allows a class to have multiple methods with the same name but different parameters, enabling different behaviors based on the input.

   - Encapsulation: Encapsulation is the concept of hiding the implementation details of an object from the outside world and only exposing the necessary information through public methods.

   - Abstraction: Abstraction is the concept of showing only the necessary information to the outside world while hiding unnecessary details.


3. Benefits of OOP:
   - Modularity: Code can be organized into distinct classes, making it easier to manage and understand.
   - Reusability: Classes can be reused across different parts of a program or in different programs, reducing redundancy.
   - Maintainability: Changes to a class can be made without affecting other parts of the codebase, as long as the interface remains consistent.
   - Abstraction: Hides complex implementation details and exposes only necessary parts of the object, making it easier to work with and understand.

*/